export function useImages() {

  const fileExtensionImages = {
    pdf: "PDF.svg",
    xlsx: "xlsx.svg",
    xls: "xlsx.svg",
    png: "png.svg",
    svg: "svg.svg",
    doc: "doc.svg",
    docx: "doc.svg",
    jpg: "jpg.svg",
    csv: "xlsx.svg",
  };

  const getFileExtensionImageUrl = (extension: string): string | undefined => {
    const imageName = fileExtensionImages[extension as keyof typeof fileExtensionImages];
    if (imageName) {
      return `https://dvlc9qcftewvt.cloudfront.net/virtuele/images/format/${imageName}`;
    }
    // Return a default image or undefined if no match
    return undefined;
  };
  
  // Expose the function and any other reactive data needed
  return {
    getFileExtensionImageUrl,
  };
}